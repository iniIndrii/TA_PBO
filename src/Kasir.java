public class Kasir {
    protected int id;
    protected String nama;
    protected int no_tlpn;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getNo_tlpn() {
        return no_tlpn;
    }

    public void setNo_tlpn(int no_tlpn) {
        this.no_tlpn = no_tlpn;
    }
}
